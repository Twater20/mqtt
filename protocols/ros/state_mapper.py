    @staticmethod
    def get_description(state_type: str, value: int) -> str:
        """
        获取状态描述

        Args:
            state_type: 状态类型
            value: 状态值

        Returns:
            状态描述
        """
        mapping = getattr(StateMapper, state_type, None)
        if mapping:
            return mapping.get(value, f"未知状态({value})")
        return f"未知类型: {state_type}"
